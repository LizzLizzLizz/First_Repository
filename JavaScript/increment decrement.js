let preincrement = 10;
console.log("nilai dari ++preincrement adalah : ",++preincrement)
console.log("sehingga nilai dari preincrement sekarang adalah : ",preincrement)

let predecrement = 10;
console.log("nilai dari --predecrement adalah : ",--predecrement)
console.log("sehingga nilai dari predecrement sekarang adalah : ",predecrement)

let postincrement = 10;
console.log("nilai dari postincrement++ adalah : ",postincrement++)
console.log("sehingga nilai dari postincrement sekarang adalah : ",postincrement)

let postdecrement = 10;
console.log("nilai dari postdecrement-- adalah : ",postdecrement--)
console.log("sehingga nilai dari postdecrement sekarang adalah : ",postdecrement)